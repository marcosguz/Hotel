package domain;

import java.util.Date;

/**
 *
 * @author Marcos
 */
public class RegistroHuesped {
    
    private Integer registroHuesped;
    private Integer cantidadPersonas;
    private TipoHabitacion tipoHabitacion;
    private Date fechaIngreso;
    private Date fechaSalida;
    private Cliente cliente;
    private Habitacion habitacion;

    public Integer getRegistroHuesped() {
        return registroHuesped;
    }

    public void setRegistroHuesped(Integer registroHuesped) {
        this.registroHuesped = registroHuesped;
    }

    public Integer getCantidadPersonas() {
        return cantidadPersonas;
    }

    public void setCantidadPersonas(Integer cantidadPersonas) {
        this.cantidadPersonas = cantidadPersonas;
    }

    public TipoHabitacion getTipoHabitacion() {
        return tipoHabitacion;
    }

    public void setTipoHabitacion(TipoHabitacion tipoHabitacion) {
        this.tipoHabitacion = tipoHabitacion;
    }

    public Date getFechaIngreso() {
        return fechaIngreso;
    }

    public void setFechaIngreso(Date fechaIngreso) {
        this.fechaIngreso = fechaIngreso;
    }

    public Date getFechaSalida() {
        return fechaSalida;
    }

    public void setFechaSalida(Date fechaSalida) {
        this.fechaSalida = fechaSalida;
    }

    public Cliente getCliente() {
        return cliente;
    }

    public void setCliente(Cliente cliente) {
        this.cliente = cliente;
    }

    public Habitacion getHabitacion() {
        return habitacion;
    }

    public void setHabitacion(Habitacion habitacion) {
        this.habitacion = habitacion;
    }
    
    
}
